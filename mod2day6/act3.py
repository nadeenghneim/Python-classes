import turtle
turtle.Screen().bgcolor("aqua")
turtle.Screen().setup(750,1000)
sp=turtle.Turtle()
size=6
while True:
    for i in range(4):
        sp.forward(size+1)
        sp.right(90)
        size=size+5
    size=size+1
turtle.done()
    

